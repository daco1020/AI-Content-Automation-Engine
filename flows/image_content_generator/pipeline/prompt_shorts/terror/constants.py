SCRIPT_PROMPT = (
    "Genera un guion para un video de TikTok/Reels de terror basado en la siguiente idea:\n"
    "'{idea}'\n\n"
    "🚨 ROL Y MISIÓN\n"
    "Eres una unidad narrativa especializada en historias tipo creepypasta cortas. "
    "Tu misión es transformar la idea en una historia que se sienta como una experiencia personal perturbadora, una advertencia, o un relato que 'supuestamente le pasó a alguien'.\n\n"
    "🚨 REGLAS CRÍTICAS DE HISTORIA\n"
    "1. EXACTAMENTE 15 escenas por fragmento de guion. La historia entera debe abarcar estas escenas progresivamente.\n"
    "2. EL FINAL DEBE SER CONSECUENCIA DE LA IDEA: La revelación debe ser algo perturbador, reinterpretar todo lo anterior y sentirse inevitable. No un final aleatorio o filosófico.\n"
    "3. NARRACIÓN CREEPYPASTA: El tono de la voz en off ('narration') debe ser cercano, inquietante y personal. "
    "Ejemplos de frases: 'Esto le pasó a alguien...', 'Nunca hagas esto si...', 'Pensé que estaba solo...'. "
    "Usa frases cortas, máximo 15 palabras por escena, ritmo rápido y mucha tensión.\n\n"
    "🧩 ESTRUCTURA NARRATIVA ADAPTADA A 15 ESCENAS\n"
    "- Escenas 1-3: Inicio inquietante (Hook y presentación de la rareza).\n"
    "- Escenas 4-8: Primera anomalía y escalada (Algo pequeño pero inquietante sucede y empeora).\n"
    "- Escenas 9-12: Falsa explicación o calma (El narrador cree haber encontrado una solución lógica).\n"
    "- Escenas 13-15: Revelación final perturbadora (Se descubre la verdadera naturaleza del problema, reinterpretando todo).\n\n"
    "🎬 COHERENCIA VISUAL (image_prompt)\n"
    "Las escenas visuales deben representar literalmente lo que describe la narración en ese instante exacto.\n"
    "El 'image_prompt' debe generar imágenes lúgubres y cinematográficas:\n"
    "- 'environment': lugares oscuros, niebla, sombras.\n"
    "- 'lighting': low key, luces parpadeantes.\n"
    "- 'style': horror photography, dark cinematic, creepypasta realism."
)
